    activeQuestion,
    availableModels,
    autocompleteInput: workflowState.autocompleteInput,
    autocompleteMode: workflowState.autocompleteMode,
    autocompleteSelectedIndex: workflowState.selectedSuggestionIndex,
    autocompleteSuggestions,
    argumentHint: workflowState.argumentHint,
    compactionSummary,
    ctrlCPressed,
    ctrlFPressed,
    currentModelId,
    currentReasoningEffort,
    displayModel,
    dynamicPlaceholder,
    handleAutocompleteIndexChange,
    handleAutocompleteSelect,
    handleBracketedPaste,
    handleModelSelect,
    handleModelSelectorCancel,
    handleMouseUp,
    handleQuestionAnswer,
    handleSubmit,
    handleTextareaContentChange,
    handleTextareaCursorChange,
    historyBufferMessages,
    initialModelId,
    inputSyntaxStyle,
    inputFocused,
    inputScrollbar,
    isEditingQueue,
    isStreaming,
    messageContent,
    messageCount: messages.length,
    messageQueue,
    messages,
    model,
    parallelAgents,
    scrollAcceleration,
    scrollboxRef,
    setIsEditingQueue,
