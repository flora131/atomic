    eventBus,
    getOwnershipTracker,
    getModelDisplayInfo,
    getSession,
    hasRunningToolRef,
    initialPrompt,
    isAgentOnlyStreamRef,
    isStreaming,
    isStreamingRef,
    loadedSkillsRef,
    mcpServerToggles,
    messages,
    modelOps,
    onCommandExecutionTelemetry,
    onModelChange,
    onResetSession,
    onSendMessage,
    onSessionMcpServersChange,
    pendingCompleteRef,
    parallelInterruptHandlerRef,
