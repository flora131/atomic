import type {
  UseChatStreamLifecycleArgs,
  UseChatStreamLifecycleResult,
} from "@/state/chat/stream/lifecycle-types.ts";
import { useChatStreamCompletion } from "@/state/chat/stream/use-completion.ts";
import { useChatStreamErrors } from "@/state/chat/stream/use-errors.ts";
import { useChatStreamStartup } from "@/state/chat/stream/use-startup.ts";

export function useChatStreamLifecycle({
  activeBackgroundAgentCountRef,
  activeStreamRunIdRef,
  agentType,
  awaitedStreamRunIdsRef,
  bindTrackedRunToMessage,
  clearDeferredCompletion,
  continueAssistantStreamInPlaceRef,
  continueQueuedConversationRef,
  currentModelRef,
  deferredCompleteTimeoutRef,
  finalizeThinkingSourceTracking,
  getActiveStreamRunId,
  hasRunningToolRef,
  isAgentOnlyStreamRef,
  isStreamingRef,
  lastStreamingContentRef,
  lastTurnFinishReasonRef,
  nextRunIdFloorRef,
  onStreamMessage,
  parallelAgentsRef,
  pendingCompleteRef,
  resetConsumers,
  resetTodoItemsForNewStream,
  resetThinkingSourceTracking,
  resolveTrackedRun,
  runningAskQuestionToolIdsRef,
  runningBlockingToolIdsRef,
  sendBackgroundMessageToAgent,
  setActiveBackgroundAgentCount,
  setBackgroundAgentMessageId,
  setIsStreaming,
  setLastStreamedMessageId,
  setMessagesWindowed,
  setParallelAgents,
  setStreamingMessageId,
  setHasRunningTool,
  shouldHideActiveStreamContent,
  startAssistantStreamRef,
  startTrackedAssistantRun,
  stopSharedStreamState,
  streamingMessageIdRef,
  streamingMetaRef,
  streamingStartRef,
  todoItemsRef,
  toolMessageIdByIdRef,
  toolNameByIdRef,
  wasInterruptedRef,
}: UseChatStreamLifecycleArgs): UseChatStreamLifecycleResult {
  const { handleStreamStartupError } = useChatStreamErrors({
    continueQueuedConversationRef,
    currentModelRef,
    finalizeThinkingSourceTracking,
    lastStreamingContentRef,
    resolveTrackedRun,
    setMessagesWindowed,
    stopSharedStreamState,
    streamingMessageIdRef,
  });

  const { handleStreamComplete } = useChatStreamCompletion({
    activeBackgroundAgentCountRef,
    activeStreamRunIdRef,
    agentType,
    awaitedStreamRunIdsRef,
    continueAssistantStreamInPlaceRef,
    continueQueuedConversationRef,
    currentModelRef,
    deferredCompleteTimeoutRef,
    finalizeThinkingSourceTracking,
    getActiveStreamRunId,
    hasRunningToolRef,
    isAgentOnlyStreamRef,
    lastStreamingContentRef,
    parallelAgentsRef,
    pendingCompleteRef,
    resolveTrackedRun,
    runningAskQuestionToolIdsRef,
    runningBlockingToolIdsRef,
    sendBackgroundMessageToAgent,
    setActiveBackgroundAgentCount,
    setBackgroundAgentMessageId,
    setLastStreamedMessageId,
    setMessagesWindowed,
    setParallelAgents,
    setHasRunningTool,
    shouldHideActiveStreamContent,
    startAssistantStreamRef,
    stopSharedStreamState,
    streamingMessageIdRef,
    streamingMetaRef,
    streamingStartRef,
    todoItemsRef,
    toolMessageIdByIdRef,
    toolNameByIdRef,
    wasInterruptedRef,
  });

  const {
    continueAssistantStreamInPlace,
    startAssistantStream,
  } = useChatStreamStartup({
    activeBackgroundAgentCountRef,
    activeStreamRunIdRef,
    bindTrackedRunToMessage,
    clearDeferredCompletion,
    handleStreamStartupError,
    hasRunningToolRef,
    isAgentOnlyStreamRef,
    isStreamingRef,
    lastStreamingContentRef,
    nextRunIdFloorRef,
    lastTurnFinishReasonRef,
    onStreamMessage,
    parallelAgentsRef,
    resetConsumers,
    resetThinkingSourceTracking,
    resetTodoItemsForNewStream,
    runningAskQuestionToolIdsRef,
    runningBlockingToolIdsRef,
    setActiveBackgroundAgentCount,
    setIsStreaming,
    setLastStreamedMessageId,
    setMessagesWindowed,
    setParallelAgents,
    setStreamingMessageId,
    startTrackedAssistantRun,
    streamingStartRef,
    toolMessageIdByIdRef,
    toolNameByIdRef,
  });

  return {
    continueAssistantStreamInPlace,
    handleStreamComplete,
    handleStreamStartupError,
    startAssistantStream,
  };
}
